// ── DB별 테스트 ─────────────────────────────────

async function testImportForDB(
    dbLabel: string,
    conn: DatabaseConnection,
    dbType: string
) {
    header(`${dbLabel} — Data Import`);
    // H2는 database 파라미터를 전달하면 안 됨 (식별자 에러)
    const db = dbType === 'h2' ? undefined : conn.config.database;

    // 1. CSV 가져오기 (기본 INSERT)
    try {
        await setupTable(conn, dbType, db);
        const csv = 'name,age,email\nAlice,30,alice@test.com\nBob,25,bob@test.com\nCharlie,35,charlie@test.com\n';
        const parsed = parseCSV(makeCsvBuffer(csv), 'test.csv');

        await assert(parsed.totalRows === 3, `[${dbLabel}] CSV 파싱 3행`);

        const schema = await conn.getTableSchema('import_test', db);
        const mappings = suggestColumnMapping(parsed.headers, schema);

        // name, age, email 모두 매핑되어야 함
        const mappedCount = mappings.filter(m => m.targetColumn !== '').length;
        await assert(mappedCount === 3, `[${dbLabel}] 자동 매핑 3개`);

        const config: DataImportConfig = {
            tableName: 'import_test',
            database: db,
            columnMapping: mappings,
            conflictStrategy: 'skip',
            batchSize: 10,
            primaryKeyColumns: schema.filter(c => c.primaryKey).map(c => c.name),
        };

        const result = await importData(conn, parsed, config);
        await assert(result.inserted === 3, `[${dbLabel}] CSV INSERT 3행 삽입`);
        await assert(result.failed === 0, `[${dbLabel}] CSV INSERT 실패 0`);
        await assert(result.executionTime > 0, `[${dbLabel}] CSV 실행 시간 > 0`);

        // DB에서 검증
        const rows = await conn.executeQuery('SELECT COUNT(*) as cnt FROM import_test', db);
        const cnt = Number(getField(rows.rows[0] as Record<string, unknown>, 'cnt'));
        await assert(cnt === 3, `[${dbLabel}] DB 검증 3행`);
    } catch (e) { fail(`[${dbLabel}] CSV 가져오기`, e); }

    // 2. JSON 가져오기
    try {
        await setupTable(conn, dbType, db);
        const data = [
            { name: 'Dan', age: 28, email: 'dan@test.com' },
            { name: 'Eve', age: 32, email: 'eve@test.com' },
        ];
        const parsed = parseJSON(makeJsonBuffer(data), 'test.json');

        const schema = await conn.getTableSchema('import_test', db);
        const mappings = suggestColumnMapping(parsed.headers, schema);

        const config: DataImportConfig = {
            tableName: 'import_test',
            database: db,
            columnMapping: mappings,
            conflictStrategy: 'skip',
            batchSize: 10,
            primaryKeyColumns: schema.filter(c => c.primaryKey).map(c => c.name),
        };

        const result = await importData(conn, parsed, config);
        await assert(result.inserted === 2, `[${dbLabel}] JSON INSERT 2행`);
    } catch (e) { fail(`[${dbLabel}] JSON 가져오기`, e); }

    // 3. Excel 가져오기
    try {
        await setupTable(conn, dbType, db);
        const buf = makeExcelBuffer(
            ['name', 'age', 'email'],
            [['Frank', 40, 'frank@test.com'], ['Grace', 22, 'grace@test.com']]
        );
        const parsed = parseExcel(buf, 'test.xlsx');

        const schema = await conn.getTableSchema('import_test', db);
        const mappings = suggestColumnMapping(parsed.headers, schema);

        const config: DataImportConfig = {
            tableName: 'import_test',
            database: db,
            columnMapping: mappings,
            conflictStrategy: 'skip',
            batchSize: 10,
            primaryKeyColumns: schema.filter(c => c.primaryKey).map(c => c.name),
        };

        const result = await importData(conn, parsed, config);
        await assert(result.inserted === 2, `[${dbLabel}] Excel INSERT 2행`);
    } catch (e) { fail(`[${dbLabel}] Excel 가져오기`, e); }

    // 4. 충돌 처리 — skip
    try {
        await setupUniqueTable(conn, dbType, db);
        // 먼저 데이터 삽입
        await conn.executeQuery("INSERT INTO import_unique (id, name, val) VALUES (1, 'Alice', 'v1')", db);
        await conn.executeQuery("INSERT INTO import_unique (id, name, val) VALUES (2, 'Bob', 'v2')", db);

        const csv = 'id,name,val\n1,Alice-dup,v1-dup\n3,Charlie,v3\n';
        const parsed = parseCSV(makeCsvBuffer(csv), 'skip.csv');

        const schema = await conn.getTableSchema('import_unique', db);
        const mappings = suggestColumnMapping(parsed.headers, schema);

        const config: DataImportConfig = {
            tableName: 'import_unique',
            database: db,
            columnMapping: mappings,
            conflictStrategy: 'skip',
            batchSize: 10,
            primaryKeyColumns: ['id'],
        };

        const result = await importData(conn, parsed, config);
        // id=1은 중복으로 skip, id=3은 삽입
        await assert(result.inserted + result.skipped === 2, `[${dbLabel}] skip 전략 총 2건 처리`);

        // id=1의 name은 원래대로 Alice (덮어쓰지 않음)
        const check = await conn.executeQuery("SELECT name FROM import_unique WHERE id = 1", db);
        const name = getField(check.rows[0] as Record<string, unknown>, 'name');
        await assert(name === 'Alice', `[${dbLabel}] skip 시 기존 데이터 보존`);

        // id=3 삽입 확인
        const check3 = await conn.executeQuery("SELECT name FROM import_unique WHERE id = 3", db);
        await assert(check3.rows.length === 1, `[${dbLabel}] skip 새 행 삽입 성공`);
    } catch (e) { fail(`[${dbLabel}] skip 충돌 처리`, e); }

    // 5. 충돌 처리 — upsert
    try {
        await setupUniqueTable(conn, dbType, db);
        await conn.executeQuery("INSERT INTO import_unique (id, name, val) VALUES (1, 'Alice', 'old')", db);

        const csv = 'id,name,val\n1,Alice,new\n2,Bob,fresh\n';
        const parsed = parseCSV(makeCsvBuffer(csv), 'upsert.csv');

        const schema = await conn.getTableSchema('import_unique', db);
        const mappings = suggestColumnMapping(parsed.headers, schema);

        const config: DataImportConfig = {
            tableName: 'import_unique',
            database: db,
            columnMapping: mappings,
            conflictStrategy: 'upsert',
            batchSize: 10,
            primaryKeyColumns: ['id'],
        };

        const result = await importData(conn, parsed, config);
        await assert(result.failed === 0, `[${dbLabel}] upsert 실패 0`);

        // id=1의 value가 'new'로 업데이트됨
        const check = await conn.executeQuery("SELECT val FROM import_unique WHERE id = 1", db);
        const val = getField(check.rows[0] as Record<string, unknown>, 'val');
        await assert(val === 'new', `[${dbLabel}] upsert 기존 행 업데이트`);

        // id=2 삽입 확인
        const check2Rows = await conn.executeQuery("SELECT name FROM import_unique WHERE id = 2", db);
        await assert(check2Rows.rows.length === 1, `[${dbLabel}] upsert 새 행 삽입`);
    } catch (e) { fail(`[${dbLabel}] upsert 충돌 처리`, e); }

    // 6. 부분 컬럼 매핑 (일부 컬럼만)
    try {
        await setupTable(conn, dbType, db);
        const csv = 'name,extra_field\nOnlyName,ignored\n';
        const parsed = parseCSV(makeCsvBuffer(csv), 'partial.csv');

        const schema = await conn.getTableSchema('import_test', db);

        // name만 매핑, extra_field는 skip
        const mappings: ColumnMapping[] = [
            { sourceColumn: 'name', targetColumn: 'name', targetType: 'varchar' },
            { sourceColumn: 'extra_field', targetColumn: '', targetType: '' },
        ];

        const config: DataImportConfig = {
            tableName: 'import_test',
            database: db,
            columnMapping: mappings,
            conflictStrategy: 'skip',
            batchSize: 10,
            primaryKeyColumns: schema.filter(c => c.primaryKey).map(c => c.name),
        };

        const result = await importData(conn, parsed, config);
        await assert(result.inserted === 1, `[${dbLabel}] 부분 매핑 1행 삽입`);

        const check = await conn.executeQuery('SELECT name, age, email FROM import_test', db);
        const row = check.rows[0] as Record<string, unknown>;
        await assert(getField(row, 'name') === 'OnlyName', `[${dbLabel}] 부분 매핑 name 정확`);
        const ageVal = getField(row, 'age');
        await assert(ageVal === null || ageVal === undefined, `[${dbLabel}] 미매핑 컬럼 NULL`);
    } catch (e) { fail(`[${dbLabel}] 부분 컬럼 매핑`, e); }

    // 7. 대용량 배치 + 진행률 콜백
    try {
        await setupTable(conn, dbType, db);
        // 50행 데이터
        const rows: unknown[][] = [];
        for (let i = 0; i < 50; i++) {
            rows.push([`User${i}`, 20 + (i % 30), `user${i}@test.com`]);
        }
        const parsed: ParsedFileData = {
            headers: ['name', 'age', 'email'],
            rows,
            totalRows: 50,
            format: 'csv',
            fileName: 'bulk.csv',
        };

        const schema = await conn.getTableSchema('import_test', db);
        const mappings = suggestColumnMapping(parsed.headers, schema);

        let progressCallCount = 0;
        let lastProgress = { current: 0, total: 0 };

        const config: DataImportConfig = {
            tableName: 'import_test',
            database: db,
            columnMapping: mappings,
            conflictStrategy: 'skip',
            batchSize: 10, // 50행 / 10 = 5 배치
            primaryKeyColumns: schema.filter(c => c.primaryKey).map(c => c.name),
        };

        const result = await importData(conn, parsed, config, (p) => {
            progressCallCount++;
            lastProgress = { current: p.current, total: p.total };
        });

        await assert(result.inserted === 50, `[${dbLabel}] 대용량 50행 삽입`);
        await assert(progressCallCount >= 5, `[${dbLabel}] 진행률 콜백 5회 이상 (${progressCallCount}회)`);
        await assert(lastProgress.current === 50, `[${dbLabel}] 최종 진행률 current=50`);
        await assert(lastProgress.total === 50, `[${dbLabel}] 최종 진행률 total=50`);
    } catch (e) { fail(`[${dbLabel}] 대용량 배치 + 진행률`, e); }

    // 8. NULL 값 처리
    try {
        await setupTable(conn, dbType, db);
        const csv = 'name,age,email\nNoAge,,\nNoEmail,25,\n';
        const parsed = parseCSV(makeCsvBuffer(csv), 'nulls.csv');

        const schema = await conn.getTableSchema('import_test', db);
        const mappings = suggestColumnMapping(parsed.headers, schema);

        const config: DataImportConfig = {
            tableName: 'import_test',
            database: db,
            columnMapping: mappings,
            conflictStrategy: 'skip',
            batchSize: 10,
            primaryKeyColumns: schema.filter(c => c.primaryKey).map(c => c.name),
        };

        const result = await importData(conn, parsed, config);
        await assert(result.inserted === 2, `[${dbLabel}] NULL 값 2행 삽입`);

        const check = await conn.executeQuery("SELECT age, email FROM import_test WHERE name = 'NoAge'", db);
        const row = check.rows[0] as Record<string, unknown>;
        const ageVal2 = getField(row, 'age');
        await assert(ageVal2 === null || ageVal2 === undefined, `[${dbLabel}] NULL age 보존`);
    } catch (e) { fail(`[${dbLabel}] NULL 값 처리`, e); }

    // 9. 특수 문자 (한국어, 싱글쿼트)
    try {
        await setupTable(conn, dbType, db);
        const csv = "name,age,email\n홍길동,30,hong@test.com\nO'Brien,40,ob@test.com\n";
        const parsed = parseCSV(makeCsvBuffer(csv), 'special.csv');

        const schema = await conn.getTableSchema('import_test', db);
        const mappings = suggestColumnMapping(parsed.headers, schema);

        const config: DataImportConfig = {
            tableName: 'import_test',
            database: db,
            columnMapping: mappings,
            conflictStrategy: 'skip',
            batchSize: 10,
            primaryKeyColumns: schema.filter(c => c.primaryKey).map(c => c.name),
        };

        const result = await importData(conn, parsed, config);
        await assert(result.inserted === 2, `[${dbLabel}] 특수 문자 2행 삽입`);

        const check = await conn.executeQuery("SELECT name FROM import_test ORDER BY age", db);
        const names = check.rows.map(r => getField(r as Record<string, unknown>, 'name'));
        await assert(names.includes('홍길동'), `[${dbLabel}] 한국어 이름 보존`);
        await assert(names.some(n => String(n).includes("O'Brien")), `[${dbLabel}] 싱글쿼트 이스케이프`);
    } catch (e) { fail(`[${dbLabel}] 특수 문자`, e); }

    // 10. 매핑 없음 오류
    try {
        await setupTable(conn, dbType, db);
        const csv = 'a,b\n1,2\n';
        const parsed = parseCSV(makeCsvBuffer(csv), 'nomapping.csv');

        const config: DataImportConfig = {
            tableName: 'import_test',
            database: db,
            columnMapping: [
                { sourceColumn: 'a', targetColumn: '', targetType: '' },
                { sourceColumn: 'b', targetColumn: '', targetType: '' },
            ],
            conflictStrategy: 'skip',
            batchSize: 10,
        };

        try {
            await importData(conn, parsed, config);
            fail(`[${dbLabel}] 매핑 없음 오류 미발생`);
        } catch (e) {
            const msg = (e as Error).message;
            await assert(msg.includes('No columns mapped'), `[${dbLabel}] 매핑 없음 오류 메시지`);
        }
    } catch (e) { fail(`[${dbLabel}] 매핑 없음 테스트`, e); }
}

