// ── Test Data ───────────────────────────────────

function makeCsvBuffer(csv: string): Uint8Array {
    return new TextEncoder().encode(csv);
}

function makeJsonBuffer(data: unknown): Uint8Array {
    return new TextEncoder().encode(JSON.stringify(data));
}

function makeExcelBuffer(headers: string[], rows: unknown[][]): Uint8Array {
    const wb = XLSX.utils.book_new();
    const ws = XLSX.utils.aoa_to_sheet([headers, ...rows]);
    XLSX.utils.book_append_sheet(wb, ws, 'Sheet1');
    return new Uint8Array(XLSX.write(wb, { type: 'array', bookType: 'xlsx' }));
}

// ── 공통 테스트 테이블 생성 ─────────────────────

async function setupTable(conn: DatabaseConnection, dbType: string, db?: string) {

    // 기존 테이블 삭제
    try { await conn.executeQuery('DROP TABLE IF EXISTS import_test', db); } catch { /* ok */ }

    let createSQL: string;
    switch (dbType) {
        case 'mysql':
            createSQL = `CREATE TABLE import_test (
                id INT PRIMARY KEY AUTO_INCREMENT,
                name VARCHAR(100) NOT NULL,
                age INT,
                email VARCHAR(200)
            )`;
            break;
        case 'postgres':
            createSQL = `CREATE TABLE import_test (
                id SERIAL PRIMARY KEY,
                name VARCHAR(100) NOT NULL,
                age INT,
                email VARCHAR(200)
            )`;
            break;
        case 'sqlite':
            createSQL = `CREATE TABLE import_test (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                name TEXT NOT NULL,
                age INTEGER,
                email TEXT
            )`;
            break;
        case 'h2':
            createSQL = `CREATE TABLE import_test (
                id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                name VARCHAR(100) NOT NULL,
                age INT,
                email VARCHAR(200)
            )`;
            break;
        default:
            throw new Error(`Unsupported: ${dbType}`);
    }

    await conn.executeQuery(createSQL, db);
}

// unique 제약 테이블
